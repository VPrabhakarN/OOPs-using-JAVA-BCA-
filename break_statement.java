// Java program by using break statement 
 
public class break_statement {
    public static void main(String args[])
    {
        int i;

        //using for loop
        for(i=1; i<=10; i++)
        {
            //using break statement
            
            if(i == 5)
            {
                break;
            }
            System.out.println(i);
        }
    } 
}
