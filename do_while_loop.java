// Java program by using do while loop

/* SYNTAX : 
             do 
             {
                // code here 
             } 
             while(condition);
*/

public class do_while_loop {
    public static void main(String args[])
    {
        int a = 1;
        do
        {
            System.out.println(a);
            a++;
        }
        while(a<=10);
    }  
}
