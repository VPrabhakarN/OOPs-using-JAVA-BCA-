// Java program by using if elseif statement

/* SYNTAX : 
            if(condition1)
            {
                // this code will execute if 'condition1' is true
            }
            else if(condition2)
            {
                // this code will execute if 'condition2' is true
            }
            else if(condition3)
            {
                // this code will execute if 'condition3' is true
            }
            else 
            {
                // this code will execute if all the conditions are false
            }
 */

public class if_elseif { 
    public static void main(String args[])
    {
        int a = 20;

        // using if else if statement
        if(a == 15)
        {
            System.out.println("Value is 10");
        } 
        else if(a == 15)
        {
            System.out.println("Value is 15");
        }
        else if(a == 20)
        {
            System.out.println("Value is 20");
        }
        else 
        {
            System.out.println("Error!");
        }
    } 
}
