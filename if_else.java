// Java program by using if else

/* SYNTAX :
            if(condition)
            {
                // code here
            }
            else 
            {
                // code here
            }
*/

public class if_else { 
    public static void main(String args[])
    {
        int age = 20;
        if(age >= 18)
        {
            System.out.println("Eligible");
        }
        else 
        {
            System.out.println("Not eligible");
        }
    }  
}
