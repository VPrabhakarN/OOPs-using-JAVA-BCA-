// Java program by using while loop

/* SYNTAX :
              while(condition)
              {
                // code here
              }
 */

public class while_loop {
    public static void main(String args[])
    {
        int a =1;
        
        // using while loop
        while(a<=10)
        {
            System.out.println(a);
            a++;
        }
    }   
}
